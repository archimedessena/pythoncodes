# reusable functions

message = input(">")

def smile_(message):
    words = message.split(" ")
    emojis = {":)": "activated",
            ":(": "deactivated"
            }

    output = " "      
    for word in words:
        output += emojis.get(word, word) + " "

    return output

print(smile_(message))


